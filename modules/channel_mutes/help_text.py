HELP_MESSAGE = """**Модуль ограничений в каналах**
Временно запрещает пользователю отправлять сообщения в **текстовом канале** и/или в его **ветках**. Остальные каналы сервера доступны.

**Команды**
• `/mute_user` — в канале/ветке наказания **или** в бот-командах с `channel`
  Пример в канале: `/mute_user user:@User duration:2h reason:спам`
  Только ветки: `/mute_user user:@User duration:1d channel:#общий scope:ветки`

**Параметр `scope`**
• `чат` — запрет только в самом канале;
• `ветки` — запрет во всех ветках канала (сам канал не затрагивается);
• `чат и ветки` — запрет и в канале, и во всех его ветках.
Если `scope` не указан: вызов в канале → `чат`, вызов в ветке → `ветки`.
Запреты на «чат» и на «ветки» независимы: у них могут быть разные сроки, и снимаются они по отдельности.


• `/unmute_user` — снять запрет
  В канале/ветке **без** `channel`: снимает все активные ограничения в этом чате (с учётом `scope`, если указан).
  В бот-командах **без** `channel`: снимает по всему серверу; при двух и более каналах — подтверждение кнопками (ephemeral).
  С `channel`: снятие в указанном канале/ветке.
  Пример: `/unmute_user user:@User scope:чат`

• `/active_mutes` — **только бот-команды**, параметр `user`
  Показывает, где действует запрет: чат, только ветки, либо чат и ветки.

• `/mute_help` — **только бот-команды**

**Примечания**
• Срок: `10m`, `2h`, `3d` (мин. 1 мин., макс. 28 дней)
• Наказание, выданное в ветке, меняет права родительского чата и действует на все его ветки
• Действия модераторов пишутся в канал логов ботов
• В публичных каналах ответы бота видит только модератор (ephemeral)
"""
